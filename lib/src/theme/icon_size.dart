/// Core Icon Size class that defines icon dimensions for the application
///
/// The icon size system provides semantically named tokens for consistent icon sizing,
/// separate from the layout spacing system.
class CoreIconSize {
  CoreIconSize._();

  /// Small icon size (16.0px)
  static const double small = 16.0;

  /// Medium icon size (20.0px)
  static const double medium = 20.0;

  /// Large icon size (24.0px)
  static const double large = 24.0;

  /// Extra large icon size (28.0px)
  static const double extraLarge = 28.0;
}
