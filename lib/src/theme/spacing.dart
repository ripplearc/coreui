/// Core Spacing class that defines spacing values for the application
///
/// The spacing system is based on a 4px grid (0.25rem with 16px base)
/// Each value is named by its multiplier of the base unit
/// https://www.figma.com/design/vugaGpii5HfgEQHPbrS3mU/Construculator-Visual-Design?node-id=7-2716&t=zZdz9haSNp4XklEm-4
class CoreSpacing {
  // Base unit (0.25rem = 4px with 16px base)
  static const double space1 = 4.0; // 0.25rem = 4px
  static const double space2 = 8.0; // 0.5rem = 8px
  static const double space3 = 12.0; // 0.75rem = 12px
  static const double space4 = 16.0; // 1rem = 16px
  static const double space5 = 20.0; // 1.25rem = 20px
  static const double space6 = 24.0; // 1.5rem = 24px
  static const double space8 = 32.0; // 2rem = 32px
  static const double space10 = 40.0; // 2.5rem = 40px
  static const double space12 = 48.0; // 3rem = 48px
  static const double space16 = 64.0; // 4rem = 64px
  static const double space20 = 80.0; // 5rem = 80px
  static const double space24 = 96.0; // 6rem = 96px
  static const double space32 = 128.0; // 8rem = 128px
  static const double space40 = 160.0; // 10rem = 160px
  static const double space48 = 192.0; // 12rem = 192px
  static const double space56 = 224.0; // 14rem = 224px
  static const double space64 = 256.0; // 16rem = 256px
}
